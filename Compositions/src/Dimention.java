public class Dimention {
    private int height;
    private double width;
    private int dept;

    public Dimention(int height, double width, int dept) {
        this.height = height;
        this.width = width;
        this.dept = dept;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public int getDept() {
        return dept;
    }

    public void setDept(int dept) {
        this.dept = dept;
    }
}
