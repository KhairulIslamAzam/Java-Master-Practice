
package javamasterclass;

public class BitwiseOperator {
    public static void main(String[] args) {
        int x=10,y=12,c;
        c=x&y;
        System.out.println("& "+c);
        
        c=x|y;
        System.out.println("| "+c);
        
        c=x^y;
        System.out.println("^ "+c);
        
    }
}
