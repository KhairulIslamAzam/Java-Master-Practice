
package javamasterclass;

public class UnaryAnd_Incre_Decre_Operator {
    public static void main(String[] args){
        int x = -10, result;
        
        //unary operator
        result = +x;
        System.out.println(result);
        result = -x;
        System.out.println(result);
        int i = 10;
        int result1;
        //increament and decrement operator
        result1 = i++;
        System.out.println(result1);
        int j = 10;
        result1 = ++j;
        System.out.println(result1);
                
         
    }
}
